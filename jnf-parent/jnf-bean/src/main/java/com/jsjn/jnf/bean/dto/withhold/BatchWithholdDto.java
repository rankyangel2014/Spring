package com.jsjn.jnf.bean.dto.withhold;

import java.math.BigDecimal;

/**
 * 批量代扣数据库对象
 * 
 * @author xiekx
 * 
 */
public class BatchWithholdDto extends BaseDTO<BatchWithholdDto> {

	private static final long serialVersionUID = 1L;

	private String id;//主键
	private String serialNo;//业务编号
	private String orgNo;//机构号
	private String loanNo;//借据号
	private String cardSignNo;//签约协议号
	private String custName;//姓名
	private String custIdNo;//身份证号
	private BigDecimal amount;//代扣金额
	private String curCode;//币种 默认为人民币（01）
	private String remark;//扣款成功后发送给客户的短信内容
	private String isValid;//是否有效（有效表示未发送单笔代扣请求，无效表示已经发送过单笔代扣请求）
	private String mid;//商户号

	public String getSerialNo() {
		return serialNo;
	}

	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}

	public String getOrgNo() {
		return orgNo;
	}

	public void setOrgNo(String orgNo) {
		this.orgNo = orgNo;
	}

	public String getLoanNo() {
		return loanNo;
	}

	public void setLoanNo(String loanNo) {
		this.loanNo = loanNo;
	}

	public String getCardSignNo() {
		return cardSignNo;
	}

	public void setCardSignNo(String cardSignNo) {
		this.cardSignNo = cardSignNo;
	}

	public String getCustName() {
		return custName;
	}

	public void setCustName(String custName) {
		this.custName = custName;
	}

	public String getCustIdNo() {
		return custIdNo;
	}

	public void setCustIdNo(String custIdNo) {
		this.custIdNo = custIdNo;
	}

	public BigDecimal getAmount() {
		return amount;
	}

	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}

	public String getCurCode() {
		return curCode;
	}

	public void setCurCode(String curCode) {
		this.curCode = curCode;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getIsValid() {
		return isValid;
	}

	public void setIsValid(String isValid) {
		this.isValid = isValid;
	}

	public String getMid() {
		return mid;
	}

	public void setMid(String mid) {
		this.mid = mid;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
}
