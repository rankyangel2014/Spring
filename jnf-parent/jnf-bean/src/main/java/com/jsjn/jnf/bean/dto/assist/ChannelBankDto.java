package com.jsjn.jnf.bean.dto.assist;

import com.jsjn.jnf.bean.dto.base.BaseDTO;

public class ChannelBankDto extends BaseDTO<ChannelBankDto>{
	
	private static final long serialVersionUID = 6L;
	private String channelId;// 渠道号码
	private String bankName;// 银行名称
	private String jnBankCode;// 金农银行code
	private String channelBankCode;// 渠道银行code
	private String maxAmount;// 单笔扣款最大限额
	private String maxAmountDay;// 银行单日扣款最大限额
	
	public String getMaxAmountDay() {
		return maxAmountDay;
	}
	public void setMaxAmountDay(String maxAmountDay) {
		this.maxAmountDay = maxAmountDay;
	}
	public String getChannelId() {
		return channelId;
	}
	public void setChannelId(String channelId) {
		this.channelId = channelId;
	}
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}
	public String getJnBankCode() {
		return jnBankCode;
	}
	public void setJnBankCode(String jnBankCode) {
		this.jnBankCode = jnBankCode;
	}
	public String getChannelBankCode() {
		return channelBankCode;
	}
	public void setChannelBankCode(String channelBankCode) {
		this.channelBankCode = channelBankCode;
	}
	public String getMaxAmount() {
		return maxAmount;
	}
	public void setMaxAmount(String maxAmount) {
		this.maxAmount = maxAmount;
	}
	
}
